# Conditional Statements
# if elif else- statement

# if - elif ladder

color = input("Enter a color in rainbow: ")

if color == 'violet':
    print("It is the first color in the rainbow")
elif color == 'indigo':
    print("It is the second color in the rainbow")
elif color == 'blue':
    print("It is the thrid color in the rainbow")
elif color == 'green':
    print("It is the fourth color in the rainbow")
elif color == 'yellow':
    print("It is the fifth color in the rainbow")
elif color == 'orange':
    print("It is the sixth color in the rainbow")
elif color == 'red':
    print("It is the seventh color in the rainbow")
else:
    print("This color is not in rainbow")
